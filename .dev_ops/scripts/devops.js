var NodeWorkspace = class {
  constructor(root) {
    this.root = root;
  }
  async findFiles(pattern, exclude, maxResults) {
    const ignore = exclude ? [exclude] : ["**/node_modules/**"];
    const entries = await (0, import_fast_glob.default)(pattern, {
      ignore,
      cwd: this.root,
      absolute: true
    });
    if (maxResults && entries.length > maxResults) {
      return entries.slice(0, maxResults);
    }
    return entries;
  }
  async readFile(path5) {
    return fs.promises.readFile(path5, "utf8");
  }
  async writeFile(path5, content) {
    await fs.promises.writeFile(path5, content, "utf8");
  }
  async exists(path5) {
    return fs.existsSync(path5);
  }
  async mkdir(path5) {
    await fs.promises.mkdir(path5, { recursive: true });
  }
};

